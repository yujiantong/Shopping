package com.web.vo;

public class KeyWord {

	private String word;
	private int num;
	
	public KeyWord() {
		super();
	}

	public KeyWord(String word, int num) {
		super();
		this.word = word;
		this.num = num;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	
}
